package com.styao.work.bean;

public class ShopClassData {
	private String Name;
	private int StoreCid;
	private int StoreId;
	private int StoreCount;
	private int AllStoreCount;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getStoreCid() {
		return StoreCid;
	}
	public void setStoreCid(int storeCid) {
		StoreCid = storeCid;
	}
	public int getStoreId() {
		return StoreId;
	}
	public void setStoreId(int storeId) {
		StoreId = storeId;
	}
	public int getStoreCount() {
		return StoreCount;
	}
	public void setStoreCount(int storeCount) {
		StoreCount = storeCount;
	}
	public int getAllStoreCount() {
		return AllStoreCount;
	}
	public void setAllStoreCount(int allStoreCount) {
		AllStoreCount = allStoreCount;
	}
	
	
}
